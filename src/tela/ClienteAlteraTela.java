    public void preencherDados(){
        campoNome.setText(cliente.getNome());
        campoEndereco.setText(cliente.getEndereco());
        campoTelefone.setText(cliente.getTelefone());
        cidadeDAO = new CidadeDAO();
        try {
            cidades = cidadeDAO.listar();
            for(Cidade c: cidades){
                comboCidades.addItem(c.getNome());
            }
            comboCidades.setSelectedItem(cliente.getCidade().getNome());
        } catch (BDException ex) {
            Logger.getLogger(ClienteAlteraTela.class.getName()).log(Level.SEVERE, null, ex);
        }           
    }
    
    public void salvar(){
        clienteDAO = new ClienteDAO();
        cliente.setNome(campoNome.getText());
        cliente.setTelefone(campoTelefone.getText());
        cliente.setEndereco(campoEndereco.getText());
        
        String cidade = (String) comboCidades.getSelectedItem();
        int id = 0;
        for(Cidade c: cidades){
            if(c.getNome().equals(cidade)){
                id = (int) c.getId();
            }
        }
        
        try {
            cliente.setCidade(cidadeDAO.buscarPorId(id));
            clienteDAO.alterar(cliente);
            JOptionPane.showMessageDialog(this,"Registro Alterado com Sucesso!");             
        } catch (BDException ex) {
            Logger.getLogger(ClienteAlteraTela.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Ocorreu um erro de banco de dados");             
        }
        
    }

